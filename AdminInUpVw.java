    public void setData()
    {
        DBConnection db=new DBConnection();
        Connection con=db.createConnection();
        if(con!=null)
        {
                String tbl="";
                if(jRadioButton1.isSelected())
                {
                    tbl+="booking_international ";
                }
                else if(jRadioButton2.isSelected())
                {
                    tbl+="booking_domestic ";
                }
                DefaultTableModel model=(DefaultTableModel)jTable2.getModel();
                try
                {
                    String sql="select * from booking_details natural join "+tbl;
                    model.setRowCount(0);
                    PreparedStatement stmt=con.prepareStatement(sql);
                    ResultSet rs=stmt.executeQuery();
                    while(rs.next())
                    {       
                        String a=rs.getString("Booking_ID");                     
                        String b=rs.getString("Total_Seats_Booked");
                        String c=rs.getString("Payment_Status");
                        String d=rs.getString("Booking_Status");
                        String e=rs.getString("booking_date");
                        String f=rs.getString("Customer_ID");
                        String g=rs.getString("Flight_ID");
                        model.addRow(new Object[]{a,b,c,d,e,f,g});
                    }
                    con.close();
                }
                catch(Exception e)
                {
                        JOptionPane.showMessageDialog(null, e);
                        System.exit(0);
                }
        }
        else
        {
            System.exit(0);
        }
    }
    
