    public void disp()
    {
        DBConnection db=new DBConnection();
        Connection con=db.createConnection();
        if(con!=null)
        {

                String tbl="";
                if(jRadioButton1.isSelected())
                {
                    tbl+="international_flights ";
                }
                else if(jRadioButton2.isSelected())
                {
                    tbl+="domestic_flights ";
                }
                DefaultTableModel model=(DefaultTableModel)jTable1.getModel();
                try
                {
                    String sql="select * from "+tbl;
                    model.setRowCount(0);
                    PreparedStatement stmt=con.prepareStatement(sql);
                    ResultSet rs=stmt.executeQuery();
                    while(rs.next())
                    {       
                        String a=rs.getString("Flight_ID");
                        String b=rs.getString("Airlines");
                        String c=rs.getString("Source");
                        String d=rs.getString("Destination");
                        String e=rs.getString("Price");
                        String f=rs.getString("No_of_Seats");
                        String g=rs.getString("Availability");
                        String h=rs.getString("flight_date");
                        String i=rs.getString("flight_time");
                        model.addRow(new Object[]{a,b,c,d,e,f,g,h,i});
                    }
                    con.close();
                }
                catch(Exception e)
                {
                        JOptionPane.showMessageDialog(null, e);
                        System.exit(0);
                }
        }
        else
        {
            System.exit(0);
        }
    }
