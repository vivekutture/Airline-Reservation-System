    private void updateCombo()
    {
        DBConnection db=new DBConnection();
        Connection con=db.createConnection();
        if(con!=null)
        {
            String tbl="";
            source.removeAllItems();
            destination.removeAllItems();
            if(dom_flight.isSelected())
            {
                tbl+="domestic_flights ";
            }
            else if(int_flight.isSelected())
            {
                tbl+="international_flights ";
            }
            try
            {
                String sql="select Distinct Source,Destination from "+tbl;
                PreparedStatement pst=con.prepareStatement(sql); 
                ResultSet rst=pst.executeQuery();
                while(rst.next())
                {
                    source.addItem(rst.getString("Source"));
                    destination.addItem(rst.getString("Destination"));
                }
            }
            catch(Exception e)
            {
                JOptionPane.showMessageDialog(this, "Radio Button Action Performed!");
            }
        }
        else
        {
            
        }
    }

