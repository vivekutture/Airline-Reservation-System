    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        DBConnection db=new DBConnection();
        Connection con=db.createConnection();
        long price=0;
        if(con!=null)
        {     
                String sql="";
                String tbl="";
                if(dom_flight.isSelected())
                {
                    tbl+="domestic_flights ";
                }
                else if(int_flight.isSelected())
                {
                    tbl+="international_flights ";
                }
                if(jRadioButton4.isSelected() || jRadioButton3.isSelected() || jRadioButton6.isSelected() || jRadioButton5.isSelected() )
                {
                    if(jRadioButton4.isSelected())
                    {
                        price=50000;
                    }
                    else if(jRadioButton3.isSelected())
                    {
                        price=100000;
                    }
                    else if(jRadioButton6.isSelected())
                    {
                        price=10000;
                    }
                    else if(jRadioButton5.isSelected())
                    {
                        price=25000;
                    }
                    sql="select * from "+tbl+"where Price <="+price+" and Source='"+source.getSelectedItem()+"' and Destination='"+destination.getSelectedItem()+"'";
                }
                else
                {
                    sql="select * from "+tbl+"where Source='"+source.getSelectedItem()+"' and Destination='"+destination.getSelectedItem()+"'";
                }
                DefaultTableModel model=(DefaultTableModel)jTable1.getModel();
                try
                {
                    int count=0;
                    model.setRowCount(0);
                    PreparedStatement stmt=con.prepareStatement(sql);
                    ResultSet rs=stmt.executeQuery();
                    while(rs.next())
                    {       
                        String a=rs.getString("Flight_ID");
                        String b=rs.getString("Airlines");
                        String c=rs.getString("Source");
                        String d=rs.getString("Destination");
                        String e=rs.getString("Price");
                        String f=rs.getString("No_of_Seats");
                        String g=rs.getString("Availability");
                        String h=rs.getString("flight_date");
                        String i=rs.getString("flight_time");
                        model.addRow(new Object[]{a,b,c,d,e,f,g,h,i});
                        count++;
                    }
                    if(count==0)
                    {
                        JOptionPane.showMessageDialog(this,"No such flights are available!");
                    }
                }
                catch(Exception e)
                {
                        System.exit(0);
                }
        }
        else
        {
            System.exit(0);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

