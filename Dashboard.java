    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        jButton2.setEnabled(true);
        DefaultTableModel model=(DefaultTableModel)jTable1.getModel();
        int SelectedRowIndex=jTable1.getSelectedRow();
        flightid.setText(model.getValueAt(SelectedRowIndex, 0).toString());
        airlines.setText(model.getValueAt(SelectedRowIndex, 1).toString());
        sources.setText(model.getValueAt(SelectedRowIndex, 2).toString());
        destinations.setText(model.getValueAt(SelectedRowIndex, 3).toString());
        prices.setText(model.getValueAt(SelectedRowIndex, 4).toString());
        noofseats.setText(model.getValueAt(SelectedRowIndex, 5).toString());
        availability.setText(model.getValueAt(SelectedRowIndex, 6).toString());
        flighttime.setText(model.getValueAt(SelectedRowIndex, 8).toString());
        String sdate=model.getValueAt(SelectedRowIndex, 7).toString();
        try
        {
            java.util.Date dt=new SimpleDateFormat("yyyy-MM-dd").parse(sdate);
            jDateChooser1.setDate(dt);
            jDateChooser1.setEnabled(false);
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
        if(Integer.parseInt(noofseats.getText())==0)
        {
            JOptionPane.showMessageDialog(this, "Seats are not available for current flight!");
            jButton2.setEnabled(false);
        }
    }//GEN-LAST:event_jTable1MouseClicked

